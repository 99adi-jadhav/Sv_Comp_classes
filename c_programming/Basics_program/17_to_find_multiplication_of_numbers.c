#include<stdio.h>
int main()
{
    int num1,num2,mul;
    printf("Enter The First Number : ");
    scanf("%d",&num1);
    printf("Enter The Second Number : ");
    scanf("%d",&num2);
    mul = num1 * num2;
    printf("Multiplication Of Two Number is : %d",mul);
    return 0;
}



